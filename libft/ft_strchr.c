#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*sp;

	sp = (char *)s;
	while (*sp != c && *sp != '\0')
		sp++;
	if (*sp == c)
		return (sp);
	else
		return (NULL);
}
