#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
	{
		ft_putstr_fd("(null)", 1);
	}
	else
	{
		while (*s)
		{
			ft_putchar_fd(*s, fd);
			s++;
		}
	}
}
